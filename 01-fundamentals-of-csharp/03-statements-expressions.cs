/* Neste arquivo estudo acerca de expressoes e declaracoes em C#, onde compreendo a diferenca de uma declaracao
e expressoes, tudo isso deve ser levado em consideracao na criacao e manutencao de um codigo
*/

// EXPRESSOES
/*São a combinação de variaveis, valores e operadores que juntos formam um unico valor, chamamos assim 
dentro de um codigo de expressoes
    *exemplo:
    42;
    3 + 4;
*/ 

// DECLARACOES
/*São as instrucoes que declaram uma variavel, um metodo ou um tipo, ou
seja, as declaracoes sao usadas para criar variaveis, metodos e tipos, e as expressoes sao usadas para atribuir valores a essas variaveis, chamar metodos e criar objetos
    *exemplo:
    int x; // declaracao de variavel
    x = 42; // expressao de atribuicao
    Console.WriteLine(x); // expressao de chamada de metodo
*/

int x; // como estou declarando uma variavel, eu estou dando uma instrucao completa para o codigo, senod assim, isso se denomina uma instrucao

x = 42;
Console.WriteLine(x); // Aqui estou chamadno um metodo que imprime o valor x, isso he denominado uma expressao







